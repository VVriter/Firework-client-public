package com.firework.client.Implementations.Utill.Render;

public class Rectangle {

    public int x;
    public int y;
    public double width;
    public double height;

    public Rectangle(int x, int y, double width, double height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
